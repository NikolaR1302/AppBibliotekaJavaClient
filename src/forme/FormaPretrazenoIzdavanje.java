    private void postaviVrednosti() {
        txtId.setText(String.valueOf(izdavanje.getId()));
        txtClan.setText(izdavanje.getClan().getIme()+" "+izdavanje.getClan().getPrezime());
        txtBibliotekar.setText(izdavanje.getBibliotekar().getIme()+" "+izdavanje.getBibliotekar().getPrezime());
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        String datum = sdf.format(izdavanje.getDatum());
        txtDatum.setText(datum);
        txtId.setEditable(false);
        txtClan.setEditable(false);
        txtBibliotekar.setEditable(false);
        txtDatum.setEditable(false);
    }

    private void postaviModel() {
        TableModelStavkeIzdavanja tmsi = new TableModelStavkeIzdavanja((ArrayList<StavkaIzdavanja>) izdavanje.getStavke());
        tblStavke.setModel(tmsi);
    }
