    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        try {
            validacijaIzbor();
            if(rbKnjiga.isSelected()){
                validacijaKnjige();
                String nazivKnjige = txtKnjiga.getText().trim();
                ArrayList<Pretraga> listaPretraga = pretraziPoKnjizi(nazivKnjige);
                
                new FormaPretrazenaIzdavanjaPoKnjizi(glavnaForma, true, listaPretraga).setVisible(true);
                
            }
            if(rbClan.isSelected()){
                validacijaClana();
                Clan clan = (Clan) cmbClan.getSelectedItem();
                int id=clan.getId();
                ArrayList<Pretraga> listaPretraga = pretraziPoClanu(id);
                
                new FormaPretrazenaIzdavanjaPoClanu(glavnaForma, true, listaPretraga).setVisible(true);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormaPretragaIzdavanja.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

   

