    private void ucitajClanove() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.UCITAJ_CLANOVE);

        KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();

        ArrayList<Clan> listaClanova = (ArrayList<Clan>) so.getOdgovor();
        cmbClan.removeAllItems();
        for (Clan clan : listaClanova) {
            cmbClan.addItem(clan);
        }
        cmbClan.setSelectedIndex(-1);
    }

    private void validacijaIzbor() throws Exception {
        String greska="";
        if(!rbClan.isSelected() && !rbKnjiga.isSelected()){
            greska = "Molimo vas izaberite kriterijum po kome želite da izvršite pretragu.";
        }
        if(!greska.isEmpty()){
            throw new Exception(greska);
        }
    }

    private void validacijaKnjige() throws Exception {
        if(txtKnjiga.getText().trim().isEmpty()){
            throw new Exception("Molimo vas unesite naziv knjige po kojoj želite da pretražite.");
        }
    }

    private void validacijaClana() throws Exception {
        if(cmbClan.getSelectedIndex()<0){
            throw new Exception("Molimo vas izaberite člana po kome želite da pretražite");
        }
    }

    private ArrayList<Pretraga> pretraziPoKnjizi(String nazivKnjige) {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.PRETRAGA_PO_KNJIZI);
        kz.setParametar(nazivKnjige);
        KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();
        ArrayList<Pretraga> listaIzdavanja= (ArrayList<Pretraga>) so.getOdgovor();
        return listaIzdavanja;
    }

    private ArrayList<Pretraga> pretraziPoClanu(int id) {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.PRETRAGA_PO_CLANU);
        kz.setParametar(id);
        KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();
        ArrayList<Pretraga> listaIzdavanja= (ArrayList<Pretraga>) so.getOdgovor();
         return listaIzdavanja;
    }
