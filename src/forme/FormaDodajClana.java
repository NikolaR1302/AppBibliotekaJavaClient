    private void sacuvaj(Clan clan) {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.SACUVAJ_CLANA);
        kz.setParametar(clan);
        KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();

        JOptionPane.showMessageDialog(this, so.getPoruka() + " - " + clan.getIme() + " " + clan.getPrezime());
    }

    private void osveziPolja() {
        txtIme.setText("");
        txtPrezime.setText("");
    }

    private void validacija() throws Exception {
        String error = "";
        if (txtIme.getText().isEmpty()) {
            error = error + "Sistem ne moze da kreira clana\nIme ne moze biti prazno!\n";
        }
        if (txtPrezime.getText().isEmpty()) {
            error = error + "Sistem ne moze da kreira clana\nPrezime ne moze biti prazno!\n";
        }
        if (!error.isEmpty()) {
            throw new Exception(error);
        }
    }
